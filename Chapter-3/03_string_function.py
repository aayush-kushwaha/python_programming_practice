story = "once upon a time there was a youtuber named Harry who uploaded python course with notes upon"
#string Functions
print(len(story)) #Prints the length of the story characters
print(story.endswith("upon")) #Checks whether the story ends with upon or not if yes then prints true
print(story.count("c")) #Counts the number of c in story
print(story.capitalize()) #Capitalizes the first letter
print(story.find("upon")) #Finds the word upon and prints its sequence number
print(story.replace("Harry", "CodeWithHarry")) #Finds Harry and replaces with CodeWithHarry